export const EUROPE_COUNTRIES = Object.keys(EUROPE_COUNTRY_REGIONS).sort((a, b) => a.localeCompare(b, "es"));

export function normalizeGeoText(value = "") {
  return String(value || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim();
}

export function canonicalCountry(value = "") {
  const raw = String(value || "").trim();
  if (!raw) return "";
  const norm = normalizeGeoText(raw);
  if (["espana", "spain", "es", "españa"].includes(norm)) return "España";
  return EUROPE_COUNTRIES.find(country => normalizeGeoText(country) === norm) || raw;
}

export function getRegionsForCountry(country = "España") {
  const canonical = canonicalCountry(country) || "España";
  return EUROPE_COUNTRY_REGIONS[canonical] || [];
}

export function isSpainCountry(country = "España") {
  return canonicalCountry(country) === "España";
}

export function cmrTypeForCountries(originCountry = "España", destinationCountry = "España") {
  const origin = canonicalCountry(originCountry) || "España";
  const destination = canonicalCountry(destinationCountry) || "España";
  return isSpainCountry(origin) && isSpainCountry(destination) ? "nacional" : "internacional";
}

export function completeOnTab(event, options = [], currentValue = "", onComplete) {
  if (event.key !== "Tab" || event.shiftKey || event.ctrlKey || event.altKey || event.metaKey) return false;
  const typed = normalizeGeoText(currentValue);
  if (!typed) return false;
  const exact = options.find(option => normalizeGeoText(option) === typed);
  const partial = exact || options.find(option => normalizeGeoText(option).startsWith(typed));
  if (!partial || partial === currentValue) return false;
  event.preventDefault();
  onComplete?.(partial);
  return true;
}

